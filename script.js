        // Interactive shadow effect on package cards
        document.querySelectorAll('.group').forEach(card => {
            card.addEventListener('mousemove', e => {
                const { left, top, width, height } = card.getBoundingClientRect();
                const x = (e.clientX - left) / width;
                const y = (e.clientY - top) / height;
                
                card.style.transform = `perspective(1000px) rotateX(${(y - 0.5) * 2}deg) rotateY(${(x - 0.5) * -2}deg)`;
            });
            
            card.addEventListener('mouseleave', () => {
                card.style.transform = 'perspective(1000px) rotateX(0) rotateY(0)';
            });
        });

        // Simple sticky header background transition
        window.addEventListener('scroll', () => {
            const nav = document.querySelector('nav');
            if (window.scrollY > 20) {
                nav.classList.add('shadow-md');
                nav.classList.replace('bg-secondary/95', 'bg-secondary');
            } else {
                nav.classList.remove('shadow-md');
                nav.classList.replace('bg-secondary', 'bg-secondary/95');
            }
        });
    


